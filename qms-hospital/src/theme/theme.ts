// Create theme options
const getThemeOptions = (mode: 'light' | 'dark', direction: 'ltr' | 'rtl'): ThemeOptions => ({
  direction,
  palette: {
    mode,
    ...(mode === 'light' ? lightPalette : darkPalette),
  },
  typography: {
    fontFamily: direction === 'rtl' 
      ? '"Cairo", "Roboto", "Helvetica", "Arial", sans-serif'
      : '"Roboto", "Helvetica", "Arial", sans-serif',
  },
  components: {
    MuiCssBaseline: {
      styleOverrides: `
        @import url('https://fonts.googleapis.com/css2?family=Cairo:wght@300;400;500;600;700&display=swap');
        @import url('https://fonts.googleapis.com/css2?family=Roboto:wght@300;400;500;700&display=swap');
      `,
    },
    MuiCard: {
      styleOverrides: {
        root: {
          borderRadius: 12,
          boxShadow: mode === 'light' 
            ? '0px 2px 4px rgba(0, 0, 0, 0.1), 0px 4px 6px rgba(0, 0, 0, 0.05)'
            : '0px 2px 4px rgba(0, 0, 0, 0.2), 0px 4px 6px rgba(0, 0, 0, 0.1)',
        },
      },
    },
    MuiButton: {
      styleOverrides: {
        root: {
          borderRadius: 8,
          textTransform: 'none',
        },
      },
    },
  },
});

// Create theme with language locale
export const createAppTheme = (
  mode: 'light' | 'dark',
  direction: 'ltr' | 'rtl',
  locale: 'ar' | 'en'
) => {
  const themeOptions = getThemeOptions(mode, direction);
  return createTheme(
    themeOptions,
    locale === 'ar' ? arEG : enUS
  );
};